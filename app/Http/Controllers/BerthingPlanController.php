     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $berthing = BerthingPlan::latest()->get();
        $international = array();
        $domestic = array();

        foreach ($berthing as $key) {
            if ($key->type == 'International') {
                $international[] = $key;
            } else {
                $domestic[] = $key;
            }
        }

        return view('berthing')->with('berthing', $berthing)->with('international', $international)->with('domestic', $domestic);
    }

